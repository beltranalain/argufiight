import { NextRequest, NextResponse } from 'next/server'
import { verifySessionWithDb } from '@/lib/auth/session-verify'
import { prisma } from '@/lib/db/prisma'
import { deductCoins } from '@/lib/belts/coin-economics'

/**
 * POST /api/admin/coins/users/[userId]/deduct
 * Deduct coins from a user (admin)
 */
export async function POST(
  request: NextRequest,
  { params }: { params: { userId: string } }
) {
  try {
    const session = await verifySessionWithDb()
    if (!session?.userId) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }

    const admin = await prisma.user.findUnique({
      where: { id: session.userId },
      select: { isAdmin: true, username: true },
    })

    if (!admin?.isAdmin) {
      return NextResponse.json({ error: 'Forbidden' }, { status: 403 })
    }

    const body = await request.json()
    const { amount, reason } = body

    if (!amount || amount <= 0) {
      return NextResponse.json(
        { error: 'Invalid amount' },
        { status: 400 }
      )
    }

    // Verify user exists
    const user = await prisma.user.findUnique({
      where: { id: params.userId },
      select: { id: true, username: true, coins: true },
    })

    if (!user) {
      return NextResponse.json({ error: 'User not found' }, { status: 404 })
    }

    if (user.coins < amount) {
      return NextResponse.json(
        { error: `Insufficient balance. User has ${user.coins} coins.` },
        { status: 400 }
      )
    }

    // Deduct coins
    await deductCoins(params.userId, amount, {
      type: 'ADMIN_DEDUCT',
      description: reason || `Coins deducted by admin ${admin.username}`,
      metadata: {
        deductedBy: session.userId,
        deductedByUsername: admin.username,
        reason: reason || 'Admin deduction',
      },
    })

    return NextResponse.json({ success: true, message: `Deducted ${amount} coins from ${user.username}` })
  } catch (error: any) {
    console.error('[API] Error deducting coins:', error)
    return NextResponse.json(
      { error: error.message || 'Failed to deduct coins' },
      { status: 500 }
    )
  }
}
