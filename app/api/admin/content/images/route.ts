import { NextRequest, NextResponse } from 'next/server'
import { verifyAdmin } from '@/lib/auth/session-utils'
import { prisma } from '@/lib/db/prisma'
import { put } from '@vercel/blob'

const MAX_FILE_SIZE = 10 * 1024 * 1024 // 10MB

// POST /api/admin/content/images - Upload image for section
export async function POST(request: NextRequest) {
  try {
    const userId = await verifyAdmin()
    if (!userId) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }

    const formData = await request.formData()
    const fileEntry = formData.get('image')
    const file = fileEntry instanceof File ? fileEntry : null
    const sectionId = (formData.get('sectionId') as string | null) || ''

    if (!file) {
      return NextResponse.json({ error: 'No file provided' }, { status: 400 })
    }

    if (!file.type.startsWith('image/')) {
      return NextResponse.json({ error: 'File must be an image' }, { status: 400 })
    }

    if (file.size > MAX_FILE_SIZE) {
      return NextResponse.json({ error: 'File size must be less than 10MB' }, { status: 400 })
    }

    // Generate unique filename
    const crypto = require('crypto')
    const fileExtension = file.name.split('.').pop() || 'jpg'
    const filename = `homepage/${crypto.randomBytes(16).toString('hex')}.${fileExtension}`

    // Upload to Vercel Blob Storage
    const bytes = await file.arrayBuffer()
    const buffer = Buffer.from(bytes)
    
    let imageUrl: string
    try {
      // Try Vercel Blob Storage first
      const blob = await put(filename, buffer, {
        access: 'public',
        contentType: file.type,
      })
      imageUrl = blob.url
    } catch (blobError) {
      // Fallback: Store as base64 data URL (not ideal but works)
      console.warn('Vercel Blob Storage not available, using base64 fallback:', blobError)
      const base64 = buffer.toString('base64')
      imageUrl = `data:${file.type};base64,${base64}`
    }

    // Get image dimensions (simplified - you might want to use sharp or similar)
    // For now, we'll just save the URL

    // Save to media library
    const media = await prisma.mediaLibrary.create({
      data: {
        url: imageUrl,
        filename: file.name,
        fileSize: file.size,
        mimeType: file.type,
        usedIn: `homepage:${sectionId}`,
        uploadedBy: userId,
      },
    })

    // Add to section (only if sectionId is provided)
    let image = null
    if (sectionId) {
      const section = await prisma.homepageSection.findUnique({
        where: { id: sectionId },
        include: { images: true },
      })

      if (!section) {
        return NextResponse.json(
          { error: 'Section not found' },
          { status: 404 }
        )
      }

      const maxOrder = section.images?.length || 0

      image = await prisma.homepageImage.create({
        data: {
          sectionId,
          url: imageUrl,
          order: maxOrder,
        },
      })
    }

    return NextResponse.json({ 
      image, 
      media,
      success: true 
    })
  } catch (error: any) {
    console.error('Failed to upload image:', error)
    const errorMessage = error.message || 'Failed to upload image'
    return NextResponse.json(
      { 
        error: errorMessage,
        details: process.env.NODE_ENV === 'development' ? error.stack : undefined
      },
      { status: 500 }
    )
  }
}

